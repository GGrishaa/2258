/* ЗАПУСК ПЕРВОЙ ЗАДАЧИ */
/* ============================================ */
.global xPortStartFirstTask
xPortStartFirstTask:
    /* === 0x666: Старт === */
    li t1, 0x10010000
    li t0, 0x00000666
    sw t0, 0(t1)
    
    /* Загружаем pxCurrentTCB и извлекаем pxTopOfStack */
    la t0, pxCurrentTCB
    lw t0, 0(t0)           /* t0 = адрес TCB */
    lw sp, 0(t0)           /* sp = pxTopOfStack (указывает на НИЗ фрейма) */
    
    /* === 0x777: Перед восстановлением === */
    li t1, 0x10010000
    li t0, 0x00000777
    sw t0, 0(t1)
    
    /* Восстанавливаем регистры (тот же порядок, что в обработчике!) */
    lw ra,   0*4(sp)
    lw gp,   1*4(sp)
    lw tp,   2*4(sp)
    lw t0,   3*4(sp)
    lw t1,   4*4(sp)
    lw t2,   5*4(sp)
    lw a0,   6*4(sp)
    lw a1,   7*4(sp)
    lw a2,   8*4(sp)
    lw a3,   9*4(sp)
    lw a4,  10*4(sp)
    lw a5,  11*4(sp)
    lw a6,  12*4(sp)
    lw a7,  13*4(sp)
    lw s0,  14*4(sp)
    lw s1,  15*4(sp)
    lw s2,  16*4(sp)
    lw s3,  17*4(sp)
    lw s4,  18*4(sp)
    lw s5,  19*4(sp)
    lw s6,  20*4(sp)
    lw s7,  21*4(sp)
    lw s8,  22*4(sp)
    lw s9,  23*4(sp)
    lw s10, 24*4(sp)
    lw s11, 25*4(sp)
    lw t3,  26*4(sp)
    lw t4,  27*4(sp)
    lw t5,  28*4(sp)
    lw t6,  29*4(sp)
    
    /* Корректируем sp: освобождаем фрейм (128 байт) */
    addi sp, sp, 128
    
    /* === 0x669: Перед запуском === */
    li t1, 0x10010000
    li t0, 0x00000669
    sw t0, 0(t1)
    
    /* Отладка: покажем ra перед прыжком */
    li t1, 0x10010000
    sw ra, 0(t1)

    .word 0x0600000B  /* maskirq x0 */
    
    jalr x0, ra, 0
    
    /* === 0x9999: ЛОВУШКА! Если вернулись сюда — ошибка === */
    li t1, 0x10010000
    li t0, 0x00009999
    sw t0, 0(t1)
1:  j 1b

