/* ОБРАБОТЧИК ПРЕРЫВАНИЙ */
/* ============================================ */
.section .text.freertos_risc_v_trap_handler
.align 4
.global freertos_risc_v_trap_handler
freertos_risc_v_trap_handler:
    /* === 0x400: Вход в обработчик === */
    li t1, 0x10010000
    li t0, 0x00000400
    sw t0, 0(t1)
    
    /* 1. Переключаемся на ISR-стек */
    la t0, xISRStackTop
    lw sp, 0(t0)           /* sp = адрес вершины ISR-стека */
    addi sp, sp, -128      /* Выделяем 32 слова = 128 байт */
    
    /* 2. Сохраняем ВСЕ регистры прерванной задачи */
    /* ПОРЯДОК: индекс 0..31 (от НИЗА фрейма к ВЕРХУ) */
    /* sp указывает на НИЗ фрейма (индекс 0 = ra) */
    
    sw ra,   0*4(sp)       /* [0]  ra  */
    sw gp,   1*4(sp)       /* [1]  gp  */
    sw tp,   2*4(sp)       /* [2]  tp  */
    sw t0,   3*4(sp)       /* [3]  t0  */
    sw t1,   4*4(sp)       /* [4]  t1  */
    sw t2,   5*4(sp)       /* [5]  t2  */
    sw a0,   6*4(sp)       /* [6]  a0  */
    sw a1,   7*4(sp)       /* [7]  a1  */
    sw a2,   8*4(sp)       /* [8]  a2  */
    sw a3,   9*4(sp)       /* [9]  a3  */
    sw a4,  10*4(sp)       /* [10] a4  */
    sw a5,  11*4(sp)       /* [11] a5  */
    sw a6,  12*4(sp)       /* [12] a6  */
    sw a7,  13*4(sp)       /* [13] a7  */
    sw s0,  14*4(sp)       /* [14] s0  */
    sw s1,  15*4(sp)       /* [15] s1  */
    sw s2,  16*4(sp)       /* [16] s2  */
    sw s3,  17*4(sp)       /* [17] s3  */
    sw s4,  18*4(sp)       /* [18] s4  */
    sw s5,  19*4(sp)       /* [19] s5  */
    sw s6,  20*4(sp)       /* [20] s6  */
    sw s7,  21*4(sp)       /* [21] s7  */
    sw s8,  22*4(sp)       /* [22] s8  */
    sw s9,  23*4(sp)       /* [23] s9  */
    sw s10, 24*4(sp)       /* [24] s10 */
    sw s11, 25*4(sp)       /* [25] s11 */
    sw t3,  26*4(sp)       /* [26] t3  */
    sw t4,  27*4(sp)       /* [27] t4  */
    sw t5,  28*4(sp)       /* [28] t5  */
    sw t6,  29*4(sp)       /* [29] t6  */
    sw zero, 30*4(sp)      /* [30] sp (оригинальный, не используется) */
    sw zero, 31*4(sp)      /* [31] unused/zero */
    
    /* === 0x410: Регистры сохранены === */
    li t1, 0x10010000
    li t0, 0x00000410
    sw t0, 0(t1)
    
    /* 3. Вызываем обработчик тика (C-код) */
    call vPortTickHandler
    
    /* === 0x44444444: После vPortTickHandler === */
    li t1, 0x10010000
    li t0, 0x44444444
    sw t0, 0(t1)
    
    /* 4. Переключаем контекст (выбор новой задачи) */
    /* vTaskSwitchContext вызывается внутри portYIELD_FROM_ISR в vPortTickHandler */
    
    /* === 0x431: После выбора контекста === */
    li t1, 0x10010000
    li t0, 0x00000431
    sw t0, 0(t1)
    
    /* 5. Загружаем стек НОВОЙ задачи */
    la t0, pxCurrentTCB
    lw t0, 0(t0)           /* t0 = адрес TCB */
    lw sp, 0(t0)           /* sp = pxTopOfStack (указывает на НИЗ фрейма, где ra) */
    
    /* === 0x440: Загрузили стек новой задачи === */
    li t1, 0x10010000
    li t0, 0x00000440
    sw t0, 0(t1)
    
    /* 6. Восстанавливаем ВСЕ регистры НОВОЙ задачи */
    /* ПОРЯДОК ДОЛЖЕН ТОЧНО СОВПАДАТЬ с сохранением выше! */
    
    lw ra,   0*4(sp)       /* [0]  ra  */
    lw gp,   1*4(sp)       /* [1]  gp  */
    lw tp,   2*4(sp)       /* [2]  tp  */
    lw t0,   3*4(sp)       /* [3]  t0  */
    lw t1,   4*4(sp)       /* [4]  t1  */
    lw t2,   5*4(sp)       /* [5]  t2  */
    lw a0,   6*4(sp)       /* [6]  a0  */
    lw a1,   7*4(sp)       /* [7]  a1  */
    lw a2,   8*4(sp)       /* [8]  a2  */
    lw a3,   9*4(sp)       /* [9]  a3  */
    lw a4,  10*4(sp)       /* [10] a4  */
    lw a5,  11*4(sp)       /* [11] a5  */
    lw a6,  12*4(sp)       /* [12] a6  */
    lw a7,  13*4(sp)       /* [13] a7  */
    lw s0,  14*4(sp)       /* [14] s0  */
    lw s1,  15*4(sp)       /* [15] s1  */
    lw s2,  16*4(sp)       /* [16] s2  */
    lw s3,  17*4(sp)       /* [17] s3  */
    lw s4,  18*4(sp)       /* [18] s4  */
    lw s5,  19*4(sp)       /* [19] s5  */
    lw s6,  20*4(sp)       /* [20] s6  */
    lw s7,  21*4(sp)       /* [21] s7  */
    lw s8,  22*4(sp)       /* [22] s8  */
    lw s9,  23*4(sp)       /* [23] s9  */
    lw s10, 24*4(sp)       /* [24] s10 */
    lw s11, 25*4(sp)       /* [25] s11 */
    lw t3,  26*4(sp)       /* [26] t3  */
    lw t4,  27*4(sp)       /* [27] t4  */
    lw t5,  28*4(sp)       /* [28] t5  */
    lw t6,  29*4(sp)       /* [29] t6  */
    /* Индексы 30-31 не восстанавливаем (sp и zero) */
    
    /* 7. Корректируем sp: освобождаем фрейм (сдвигаем вверх на 128 байт) */
    addi sp, sp, 128
    
    /* === 0x460: Перед возвратом в задачу === */
    li t1, 0x10010000
    li t0, 0x00000460
    sw t0, 0(t1)
    
    /* === 0x461: Показываем ra перед прыжком (отладка) === */
    li t1, 0x10010000
    sw ra, 0(t1)
    
    /* === 0x499: Маркер перед возвратом === */
    li t1, 0x10010000
    li t0, 0x00000499
    sw t0, 0(t1)

    .word 0x0600000B  /* maskirq x0 */

    jalr x0, ra, 0
    
    /* === 0x9999: ЛОВУШКА! Если вернулись сюда — ошибка возврата === */
    li t1, 0x10010000
    li t0, 0x00009999
    sw t0, 0(t1)
1:  j 1b

