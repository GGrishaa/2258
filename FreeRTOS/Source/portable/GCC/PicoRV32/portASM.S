/* ИНИЦИАЛИЗАЦИЯ СТЕКА ЗАДАЧИ */
/* ============================================ */
.section .text
.global pxPortInitialiseStack
pxPortInitialiseStack:
    /* === 0x1100: Вход === */
    li t1, 0x10010000
    li t0, 0x00001100
    sw t0, 0(t1)
    
    /* Отладка: показываем входные параметры */
    li t1, 0x10010000
    sw a0, 0(t1)           /* a0 = pxTopOfStack (вершина, высокий адрес) */
    li t1, 0x10010000
    sw a1, 0(t1)           /* a1 = pxCode (адрес задачи) */
    li t1, 0x10010000
    sw a2, 0(t1)           /* a2 = pvParameters */
    
    li t0, 0               /* Ноль для большинства регистров */
    
    /* 
     * Стек растёт ВНИЗ: от высоких адресов к низким.
     * a0 = pxTopOfStack = максимальный адрес (вершина).
     * Мы записываем 32 слова (128 байт) со смещениями ОТРИЦАТЕЛЬНЫМИ от a0.
     * 
     * ПОРЯДОК (индекс 0..31 от НИЗА фрейма к ВЕРХУ):
     *   Индекс 0 (низ): ra = адрес задачи
     *   Индекс 31 (верх): unused/zero
     * 
     * ВОЗВРАЩАЕМ: указатель на НИЗ фрейма (индекс 0), чтобы
     * lw ra, 0*4(sp) прочитал адрес задачи.
     */
    
    /* === Записываем регистры ОТ ВЕРХА фрейма к НИЗУ === */
    /* Индекс 31 (верх): unused */
    sw zero, -1*4(a0)
    
    /* Индекс 30: оригинальный sp (вершина стека) */
    sw a0, -2*4(a0)
    
    /* Индекс 29: t6 */
    sw t0, -3*4(a0)
    /* Индекс 28: t5 */
    sw t0, -4*4(a0)
    /* Индекс 27: t4 */
    sw t0, -5*4(a0)
    /* Индекс 26: t3 */
    sw t0, -6*4(a0)
    
    /* Индекс 25: s11 */
    sw t0, -7*4(a0)
    /* Индекс 24: s10 */
    sw t0, -8*4(a0)
    /* Индекс 23: s9 */
    sw t0, -9*4(a0)
    /* Индекс 22: s8 */
    sw t0, -10*4(a0)
    /* Индекс 21: s7 */
    sw t0, -11*4(a0)
    /* Индекс 20: s6 */
    sw t0, -12*4(a0)
    /* Индекс 19: s5 */
    sw t0, -13*4(a0)
    /* Индекс 18: s4 */
    sw t0, -14*4(a0)
    /* Индекс 17: s3 */
    sw t0, -15*4(a0)
    /* Индекс 16: s2 */
    sw t0, -16*4(a0)
    /* Индекс 15: s1 */
    sw t0, -17*4(a0)
    /* Индекс 14: s0 */
    sw t0, -18*4(a0)
    
    /* Индекс 13: a7 */
    sw t0, -19*4(a0)
    /* Индекс 12: a6 */
    sw t0, -20*4(a0)
    /* Индекс 11: a5 */
    sw t0, -21*4(a0)
    /* Индекс 10: a4 */
    sw t0, -22*4(a0)
    /* Индекс 9: a3 */
    sw t0, -23*4(a0)
    /* Индекс 8: a2 */
    sw t0, -24*4(a0)
    /* Индекс 7: a1 */
    sw t0, -25*4(a0)
    /* Индекс 6: a0 = pvParameters */
    sw a2, -26*4(a0)
    
    /* Индекс 5: t2 */
    sw t0, -27*4(a0)
    /* Индекс 4: t1 */
    sw t0, -28*4(a0)
    /* Индекс 3: t0 */
    sw t0, -29*4(a0)
    /* Индекс 2: tp */
    sw t0, -30*4(a0)
    /* Индекс 1: gp */
    sw t0, -31*4(a0)
    
    /* Индекс 0 (НИЗ ФРЕЙМА): ra = адрес задачи */
    sw a1, -32*4(a0)
    
    /* === 0x1104: Выход === */
    li t1, 0x10010000
    li t0, 0x00001104
    sw t0, 0(t1)
    
    /* Возвращаем указатель на НИЗ фрейма (индекс 0 = ra) */
    /* Чтобы lw ra, 0*4(sp) прочитал адрес задачи */
    addi a0, a0, -32*4
    ret

